/*
	CountTransactionsFromSYMNotAlreadyIn.sql
	Find count of transactions not already in Hub_Transaction from SYM dump
*/
SELECT count(*)
FROM SYM.SAVINGSTRANSACTION A
	LEFT JOIN sym_vault1.Hub_Transaction B
		ON A.PARENTACCOUNT = B.PARENT_ACCT AND A.PARENTID = B.ID AND A.SEQUENCENUMBER = B.SEQUENCE_NUM AND A.POSTDATE = B.POST_DATE AND B.CATEGORY = 'S'
WHERE A.COMMENTCODE = 0 AND B.PARENT_ACCT IS NULL AND B.ID IS NULL AND B.SEQUENCE_NUM IS NULL AND B.POST_DATE IS NULL